function buildPackageJson(brief) {
  const slug = brief.topic.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
  return {
    name: `syllabus-${slug}`,
    private: true,
    version: '1.0.0',
    type: 'module',
    scripts: {
      dev: 'vite',
      build: 'vite build',
      preview: 'vite preview'
    },
    dependencies: {
      'react': '^18.3.1',
      'react-dom': '^18.3.1',
      'react-router-dom': '^6.28.0',
      'framer-motion': '^11.11.0',
      'prismjs': '^1.29.0',
      'lucide-react': '^0.453.0'
    },
    devDependencies: {
      '@vitejs/plugin-react': '^4.3.0',
      'vite': '^6.0.0',
      'tailwindcss': '^3.4.0',
      '@tailwindcss/typography': '^0.5.15',
      'autoprefixer': '^10.4.20',
      'postcss': '^8.4.47'
    }
  };
}

