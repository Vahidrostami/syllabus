/**
 * Reference Template — React Project Scaffolder
 * 
 * This file is a reference implementation that the React Developer agent
 * can look at for project structure, component patterns, and CSS setup.
 * It can also be run directly to scaffold a blank project skeleton.
 * 
 * The agent doesn't HAVE to use this — it can write everything from scratch.
 * But this gives it a proven starting point.
 */

import fs from 'fs-extra';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

/**
 * Scaffold the complete React tutorial project from a LearningBrief.
 * In agentic mode, each file's content is generated by the agent pipeline.
 * In CLI mode, this copies from templates and injects the brief's config.
 */
export async function scaffoldProject(brief) {
  const outputDir = path.resolve(brief.output);
  await fs.ensureDir(outputDir);

  // Write package.json
  await fs.writeJson(path.join(outputDir, 'package.json'), buildPackageJson(brief), { spaces: 2 });

  // Write Vite config
  await fs.writeFile(path.join(outputDir, 'vite.config.js'), buildViteConfig());

  // Write Tailwind configs
  await fs.writeFile(path.join(outputDir, 'tailwind.config.js'), buildTailwindConfig(brief));
  await fs.writeFile(path.join(outputDir, 'postcss.config.js'), buildPostCSSConfig());

  // Write index.html
  await fs.writeFile(path.join(outputDir, 'index.html'), buildIndexHtml(brief));

  // Create src directories
  const srcDirs = [
    'src', 'src/components', 'src/components/layout', 'src/components/lesson',
    'src/components/quiz', 'src/components/progress', 'src/components/ui',
    'src/hooks', 'src/data', 'src/data/lessons', 'src/data/quizzes',
    'src/lib', 'src/pages'
  ];
  for (const dir of srcDirs) {
    await fs.ensureDir(path.join(outputDir, dir));
  }

  // Write main entry files
  await fs.writeFile(path.join(outputDir, 'src/main.jsx'), buildMainJsx());
  await fs.writeFile(path.join(outputDir, 'src/index.css'), buildGlobalCSS(brief));
  await fs.writeFile(path.join(outputDir, 'src/App.jsx'), buildAppJsx());

  // Write placeholder syllabus data
  await fs.writeJson(
    path.join(outputDir, 'src/data/syllabus.json'),
    buildPlaceholderSyllabus(brief),
    { spaces: 2 }
  );

  // Write hooks
  await fs.writeFile(path.join(outputDir, 'src/hooks/useProgress.js'), buildUseProgress());
  await fs.writeFile(path.join(outputDir, 'src/hooks/useQuiz.js'), buildUseQuiz());

  // Write lib files
  await fs.writeFile(path.join(outputDir, 'src/lib/constants.js'), buildLibConstants(brief));
  await fs.writeFile(path.join(outputDir, 'src/lib/utils.js'), buildLibUtils());

  return outputDir;
}

